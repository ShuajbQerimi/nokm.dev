// Get the modal
var myModal1 = document.getElementById("myModal1");

// Get the button that opens the modal
var myBtnmodal1 = document.getElementById("myBtnmodal1");

// Get the <span> element that closes the modal
var closemodal1 = document.getElementsByClassName("closemodal1")[0];

// When the user clicks the button, open the modal 
myBtnmodal1.onclick = function () {
    myModal1.style.opacity = 1;
    myModal1.style.display = "block";
    myModal1.style.transition = "opacity 5s ease-in-out";

}

// When the user clicks on <span> (x), close the modal
closemodal1.onclick = function () {
    myModal1.style.opacity = 0;
    myModal1.style.display = "none";
    myModal1.style.transition = "opacity 5s ease-in-out";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function (event) {
    if (event.target == myModal1) {
        myModal1.style.display = "none";
        myModal1.style.transition = "opacity 5s ease-in-out";
    }
}

